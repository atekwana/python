
story = "once upon a time"

print(story.capitalize()) # only capitalizes the first character

print(story.title()) # only capitalizes the first character of each word

print(story.upper()) # capitalizes the entire word

print("HELLO".lower())

print("AbCdE".swapcase()) # reverses the cases 

# method training
print("BENJAMIN FRANKLIN".lower().title())

print(story) # strings are immutable so it will print the original story variable

story = story.title() # now story changes by generating a new string

print(story)






