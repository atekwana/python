# casting

# integer casting
print(int(3.14))

print(int(3.99))

print(int("3"))

print(int(3))

# float casting
print(float(5))

print(float("10.32"))

print(float(5.23))

# string casting
print(str(5.35))

print(str(5))

print(str("hello"))

print(str(5) + " hello")

# automatic type conversion
# mixed type conversion
print (3 + 3)

print(4.3 + 5.7)

print(3 + 5.8)